	public function afterInstall() {
		$sM = ClassRegistry::init('Offerwalls.OfferwallsSettings');
		if(!$sM->keyExists('offerwalls')) {
			$settings = array(
				'offerwalls' => array(
					'credit' => 'account',
				),
			);
			if(!$sM->store(array($sM->alias => $settings), array_keys($settings))) {
				return false;
			}
		}

		$memberships = array_keys(ClassRegistry::init('Membership')->find('list'));
		$mO = ClassRegistry::init('Offerwalls.OfferwallsMembership');
		$to_unset = Hash::extract($mO->find('all', array('fields' => array('id', 'membership_id'))), 'OfferwallsMembership.membership_id');
		$memberships = array_diff($memberships, $to_unset);

		$toAdd = array();

		foreach($memberships as $membership_id) {
			$toAdd[] = array(
				'membership_id' => $membership_id,
				'point_ratio' => 0,
				'delay' => 100,
			);
		}

		if(!empty($toAdd)) {
			if(!$mO->saveMany($toAdd)) {
				return false;
			}
		}

		return true;
	}

